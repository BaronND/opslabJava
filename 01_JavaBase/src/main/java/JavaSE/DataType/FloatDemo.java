package JavaSE.DataType;

public class FloatDemo {

    public static void main(String[] args) {
        //注意在Java中要给float变量复制时 例如将3.4复制给f_var需要向下这样写
        float f_var = 3.4f;

        System.out.println(f_var);

        //这样写的3.4默认是double类型的
        //		float 	f_var1 = 3.4;
    }

}

